package problem.calc;

public class Calculater {
	int result;

	// 덧셈
	public int sum(int first, int second) {
		return first + second;

	}

	// 뺄셈
	public int sub(int first, int second) {
		return first - second;

	}

	// 곱셈
	public int multi(int first, int second) {
		return first * second;
	}

	// 나눗셈
	public int div(int first, int second) {
		return first / second;

	}
}
